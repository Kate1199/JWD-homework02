package by.epam.kisel.utility;

public class ExcessWeightException extends Exception {

	public ExcessWeightException() {
		super();
	}

	public ExcessWeightException(String message) {
		super(message);
	}

	public ExcessWeightException(String message, Throwable cause) {
		super(message, cause);
	}

	public ExcessWeightException(Throwable cause) {
		super(cause);
	}
}
